package Easy.CoffeeMachine;

public class Coffee {
    final String name;
    final int water;
    final int milk;
    final int beans;
    final int money;

    public Coffee(String name, int water, int milk, int beans, int money) {
        this.name = name;
        this.water = water;
        this.milk = milk;
        this.beans = beans;
        this.money = money;
    }
}
